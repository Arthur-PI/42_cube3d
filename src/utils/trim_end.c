#include "utils.h"

void	trim_end(char *s)
{
	uint	i;

	i = ft_strlen(s) - 1;
	while (i > 0 && ft_isspace(s[i]))
	{
		s[i] = 0;
		i--;
	}
	if (i == 0 && ft_isspace(s[i]))
		s[i] = 0;
}
